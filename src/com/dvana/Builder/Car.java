package com.dvana.Builder;

public class Car {
    public String color;
    public String brand;
    public int numberOfDoors;
    public boolean isConvertible;

    public Car(String color, String brand, int numberOfDoors, boolean isConvertible) {
        this.color = color;
        this.brand = brand;
        this.numberOfDoors = numberOfDoors;
        this.isConvertible = isConvertible;
    }
}
